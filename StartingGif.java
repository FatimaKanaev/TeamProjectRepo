import greenfoot.*;

public class StartingGif extends Actor
{
    private GreenfootImage[] images;
    private int currentIndex = 0;

    public StartingGif() {
        images = new GreenfootImage[64];

        for (int i = 0; i < images.length; i++) {
            images[i] = new GreenfootImage((i + 1) + ".jpg");
        }

        setImage(images[currentIndex]);
    }

    public void act() {
        if (currentIndex < images.length - 1 && Greenfoot.getRandomNumber(100) < 60) {
            currentIndex++;
            setImage(images[currentIndex]);
        }
    }
}